public class DataTypes {
    public static void main(String args[]) {
        byte byteNum = 13; // -128 and 127
        short shortNum = 500; // -32768 to 32767
        int myNum = 5;
        double myNumDouble = (double) myNum;
        System.out.println(myNumDouble);
        long longNum = 510L;
        float floatNum = 5.99f;
        double doubleNum = 4.2d;
        char myLetter = 'a';
        boolean isRaining = true;
        float f1 = 35e3f;
        System.out.println(f1);
    }
}